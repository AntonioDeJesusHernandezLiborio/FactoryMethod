    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String opString = cmbFigura.getModel().getSelectedItem().toString();
        
        switch(opString){
            case "cuadrado":
                datos = new Double[1];
                datos[0]= Double.parseDouble(txtLadoA.getText());
                figura = fabricaFiguras.crear("cuadrado");
                calculo();
                break;
            case "rectangulo":
                datos = new Double[2];
                datos[0]= Double.parseDouble(txtLadoA.getText());
                datos[1]= Double.parseDouble(txtLadoB.getText());
                figura = fabricaFiguras.crear("rectangulo");
                calculo();
                break;
            case "triangulo":
                datos = new Double[2];
                datos[0]= Double.parseDouble(txtLadoA.getText());
                datos[1]= Double.parseDouble(txtLadoB.getText());
                figura = fabricaFiguras.crear("triangulo");
                calculo();
                break;
            case "circulo":
                datos = new Double[1];
                datos[0]= Double.parseDouble(txtLadoA.getText());
                figura = fabricaFiguras.crear("circulo");
                calculo();
                break;
            default:
                JOptionPane.showMessageDialog(null, "Opcion no valida");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
