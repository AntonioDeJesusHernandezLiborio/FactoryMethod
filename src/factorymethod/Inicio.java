    private void cmbFiguraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbFiguraActionPerformed
        String opString = cmbFigura.getModel().getSelectedItem().toString();
        switch(opString){
            case "cuadrado":
                habilitarUno();
                ladoA.setText("Lado");
                break;
            case "rectangulo":
                habilitarTodo();
                ladoA.setText("Lado A");
                ladoB.setText("Lado B");
                
                break;
            case "triangulo":
                habilitarTodo();
                ladoA.setText("Altura");
                ladoB.setText("Base");
                break;
            case "circulo":
                habilitarUno();
                ladoA.setText("Radio");
                break;
            default:
                JOptionPane.showMessageDialog(null, "Opcion no valida");
        }
    }//GEN-LAST:event_cmbFiguraActionPerformed

    private void habilitarTodo(){
        txtLadoA.setVisible(true);
        txtLadoB.setVisible(true);
        ladoA.setVisible(true);
        ladoB.setVisible(true);
    }
    
    private void habilitarUno(){
        txtLadoA.setVisible(true);
        txtLadoB.setVisible(false);
        ladoA.setVisible(true);
        ladoB.setVisible(false);
    }
